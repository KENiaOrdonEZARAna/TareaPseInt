// Proceso: resolver el Algoritmo 
// salida: respuesta del Algoritmo 

const leer=require() ('prompt sync')()
const escribir=console()

class decada_boda {
decada_boda() {
function decada_boda() {
	// definir las variables 
	var decada = new Number();
	document.write("Digite una decada",'<BR/>');
	decada = Number(prompt());
	// calcular el Algoritmo 
	switch (decada) {
	case 10:
		escribir.log("Boda de hojalata ",'<BR/>');
		break;
	case 20:
		escribir.log("Boda de porcelana",'<BR/>');
		break;
	case 30:
		escribir.log("Boda de perlas",'<BR/>');
		break;
	case 40:
		escribir.log("Boda de rubi",'<BR/>');
		break;
	case 50:
		escribir.log("Boda de oro",'<BR/>');
		break;
	case 60:
		escribir.log("Boda de diamante",'<BR/>');
		break;
	default:
		escribir.log("Decada no existe",'<BR/>');
	}
}
}
}
const ejer=new decada_boda()
ejer.decada_boda()